package webprogramming.dataspb;

public class ConcertOrganization {
	private int number;
	private String fullName;
	private String shortName;
	private String type;
	private String address;
	private String legalAddress;
	private String district;
	private String nearestSubway;
	private String director;
	private String phone;
	private String fax;
	private String numberOfSeats;
	private String email;
	private String municipality;
	private long inn;
	private long ogrn;

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getShortName() {
		return shortName;
	}

	public void setShortName(String shortName) {
		this.shortName = shortName;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getLegalAddress() {
		return legalAddress;
	}

	public void setLegalAddress(String legalAddress) {
		this.legalAddress = legalAddress;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getNearestSubway() {
		return nearestSubway;
	}

	public void setNearestSubway(String nearestSubway) {
		this.nearestSubway = nearestSubway;
	}

	public String getDirector() {
		return director;
	}

	public void setDirector(String director) {
		this.director = director;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getNumberOfSeats() {
		return numberOfSeats;
	}

	public void setNumberOfSeats(String numberOfSeats) {
		this.numberOfSeats = numberOfSeats;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMunicipality() {
		return municipality;
	}

	public void setMunicipality(String municipality) {
		this.municipality = municipality;
	}

	public long getInn() {
		return inn;
	}

	public void setInn(long inn) {
		this.inn = inn;
	}

	public long getOgrn() {
		return ogrn;
	}

	public void setOgrn(long ogrn) {
		this.ogrn = ogrn;
	}

}
